

# Utilizamos la función `range(1, 11)` para generar una secuencia de números del 1 al 10.
for numero in range(1, 11):
    # Calculamos el cuadrado del número actual.
    cuadrado = numero ** 2
    
    # Imprimimos el número actual y su cuadrado.
    print(f"El número {numero} tiene como cuadrado {cuadrado}.")
